def draw_building(x, Width, Height, Color):
    # Buildings are drawn from their center in cs1graphics
    # We calculate the y-center so the bottom sits at y=700
    y_center = 450 - (Height/ 2)
    b = Rectangle(Width,Height, Point(x, y_center))
    b.setFillColor(Color)
    b.setBorderColor('black')
    paper.add(b)
    # Add a few "modern" windows (long vertical strips or grids)
    window_width = Width * 0.3
    for i in range(1, 4):
        win = Rectangle(window_width, Height - 40, Point(x-(i-2)*window_width, y_center))
        win.setFillColor('lightcyan')
        win.setDepth(10) # Ensure windows stay in front
        paper.add(win)
