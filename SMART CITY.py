for i in range(steps):
    angle = math.pi - (i * math.pi / steps)
    x = center_x + radius * math.cos(angle)
    y = center_y - radius * math.sin(angle)
    sun.moveTo(x, y)

    car1.moveTo(a, h)
    car2.moveTo(n, f)
    cloud1.moveTo(j, k)
    cloud3.moveTo(l, m)

    a += 7
    n -= 8
    j -= 5.5
    l += 6

    # Normalize sun height (0 = bottom, 1 = top)
    sun_height = max(0, min(1, (center_y - y) / radius))

    # Sky starts blue and fades to black
    r = int(0 * sun_height)
    g = int(191 * sun_height)
    b = int(255 * sun_height)

    paper.setBackgroundColor((r, g, b))
    Building5_2.setFillColor((r, g, b))
    Building5_2.setBorderColor((r, g, b))


    # ---- SUNSET COLOR ----
    if i > steps // 2:
        sun.setFillColor((255, max(0, 255 - (i - steps//2) * 2), 0))
        bus1.moveTo(c, e)
        c -= 10

    if i > steps // 1.5:
        car3.moveTo(v, f)
        bus2.moveTo(d, z)
        v -= 10.3
        o=7
        d += 10.7
    time.sleep(0.01)
